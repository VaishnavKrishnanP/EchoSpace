class TrieNode {
  Map<String, TrieNode> children = {};
  bool isEndOfWord = false;
}

class Trie {
  TrieNode root = TrieNode();

  // Insert a word into the Trie
  void insert(String word) {
    TrieNode node = root;
    for (var char in word.runes) {
      String charStr = String.fromCharCode(char).toLowerCase();
      if (!node.children.containsKey(charStr)) {
        node.children[charStr] = TrieNode();
      }
      node = node.children[charStr]!;
    }
    node.isEndOfWord = true;
  }

  // Check if a word exists in the Trie
  bool contains(String word) {
    TrieNode node = root;
    for (var char in word.runes) {
      String charStr = String.fromCharCode(char).toLowerCase();
      if (!node.children.containsKey(charStr)) {
        return false;
      }
      node = node.children[charStr]!;
    }
    return node.isEndOfWord;
  }

  // Check if any foul word exists as a subword in the input text
  bool containsFoulLanguage(String text) {
    text = text.toLowerCase();
    for (int i = 0; i < text.length; i++) {
      TrieNode node = root;
      for (int j = i; j < text.length; j++) {
        String char = text[j];
        if (!node.children.containsKey(char)) {
          break;
        }
        node = node.children[char]!;
        if (node.isEndOfWord) {
          return true;
        }
      }
    }
    return false;
  }
}

